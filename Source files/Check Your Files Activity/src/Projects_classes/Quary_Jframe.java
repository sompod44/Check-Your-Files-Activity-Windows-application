    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jTextField2.getText().equals("")) {
            jLabel5.setText("Select a row first. Or add Files.");
        } else {
            String savedatetime = "lastdate";
            String savelastchangeno = "lastno";
            if (permission == 0) {
                int a = Integer.parseInt(String.valueOf(jTable1.getSelectedRow()));
                String path = jTable1.getValueAt(a, 1).toString();
                String pathmake = path.replace("\\", "\\\\");

                File file1 = new File(pathmake);
                File file11 = new File(file1.getParent());
                String r = "row" + a;
                String name = r + r + a;
                Result_slowing.count_files = String.valueOf(file11.listFiles().length);
                if (!prefs.get(name, "").equals(String.valueOf(file1.lastModified()))) {
                    int x = prefs.getInt(savelastchangeno + a, 100);
                    ++x;
                    prefs.putInt(savelastchangeno + a, x);
                    Result_slowing.count_modifi = String.valueOf(prefs.getInt(savelastchangeno + a, x));
                    prefs.put(name, String.valueOf(file1.lastModified()));
                    String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());

                    String[] d = timeStamp.split(" ");
                    String datetime = "Date : " + d[0] + " Time : " + d[1];
                    prefs.put(savedatetime + a, datetime);
                } else {
                    Result_slowing.count_modifi = String.valueOf(prefs.getInt(savelastchangeno + a, 100));

                }
                Result_slowing.store_data = prefs.get(savedatetime + a, "null");
                this.setVisible(false);
                new Result_slowing().setVisible(true);

            } else {
                jLabel5.setText("Save your table.");
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

