        jLabel5.setText("");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        prefs.putInt("row_total_number", jTable1.getRowCount());
        rowno = new int[jTable1.getRowCount()];
        String savelastchangeno = "lastno";
